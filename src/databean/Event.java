package databean;


public class Event {
	String action;
	String event;
	
	public String getAction() {
		return action;
	}
	public String getEvent() {
		return event;
	}
	public void setAction(String action) {
		this.action = action;
	}
	public void setEvent(String event) {
		this.event = event;
	}
}
